uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  if(n < 0)
    n = 0;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// set current->mask
uint64 sys_trace(void)
{
  int mask;
  argint(0,&mask);
  struct proc* current=myproc();
  current->mask=mask;
  return 0;
}

extern int count_freemem();
extern int count_nproc();
uint64 sys_info(void)
{
  struct sysinfo sysinfo_p;
  uint64 ret_p;
  argaddr(0,&ret_p);
  sysinfo_p.freemem=count_freemem();
  sysinfo_p.nproc=count_nproc();


  if(copyout(myproc()->pagetable, ret_p, (char *)&sysinfo_p, sizeof(sysinfo_p)) < 0)
      return -1;
  return 0;
}